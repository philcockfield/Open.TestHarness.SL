using System.Windows.Input;

namespace Open.Core.Common
{
    public static partial class KeyExtensions
    {
        /// <summary>Determines whether the specified key represents a number.</summary>
        /// <param name="key">The key to examine.</param>
        public static bool IsNumeric(this Key key)
        {
            switch (key)
            {
                case Key.D0:
                case Key.D1:
                case Key.D2:
                case Key.D3:
                case Key.D4:
                case Key.D5:
                case Key.D6:
                case Key.D7:
                case Key.D8:
                case Key.D9:

                case Key.NumPad0:
                case Key.NumPad1:
                case Key.NumPad2:
                case Key.NumPad3:
                case Key.NumPad4:
                case Key.NumPad5:
                case Key.NumPad6:
                case Key.NumPad7:
                case Key.NumPad8:
                case Key.NumPad9:
                    return true;
            }
            return false;
        }
    }


}
